#include "ft_printf.h"

static size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

int	ft_putstr(char *s, int fd)
{
	if (!s)
		return (ft_putstr("(null)", fd));
	return (write(fd, s, ft_strlen(s)));
}
